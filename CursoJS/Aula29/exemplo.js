const n = 10;

if (n <= 10) {
  console.log("O numer é menor ou igual a 10");
}

if (n >= 0 && n <= 5) {
  console.log("O numer está entre 0 e 5");
} else if (n >= 6 && n <= 8) {
  console.log("O numer está entre 6 e 8");
} else if (n >= 9 && n <= 11) {
  console.log("O numero está entre 9 e 11");
} else {
  console.log("O numero naõ está entre 0 e 11");
}
